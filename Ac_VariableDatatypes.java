public class Ac_VariableDatatypes {
    public static void main (String args[]){
        int age = 25; // This is an integer variable
        double salary = 50000.50; // This is a double variable
        char grade = 'A'; // This is a character variable
        String name = "John Doe"; // This is a string variable

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Salary: $" + salary);
        System.out.println("Grade: " + grade);        
    }
}
