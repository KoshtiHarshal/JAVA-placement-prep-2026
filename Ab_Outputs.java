class Ab_Outputs{
    public static void main(String args[]){
        System.out.println("Welcome to Java programming.");
        System.out.println("This is an example of outputs in Java.");
        System.out.println("There are different ways to display outputs in Java.");
        System.out.print("1st-You can use System.out.print() to print without a new line.");
        System.out.println("2nd-You can use System.out.println() to print a line of text.");
        System.out.println("3rd-You can use \"\\n\" in the string to create a new line.\n This is a new line using \"\\n\" in the same print statement.");
        System.out.println("4th-You can use System.out.printf() to format your output.");
        System.out.println("Printing Pattern using System.out.println():");
        System.out.println("* ");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");
    }
}